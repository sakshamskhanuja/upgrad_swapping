public class Main {
    public static void main(String[] args) {
        int a, b;
        a = 1;
        b = 2;

        // Code exchanges values of a and b.
        int c = a;
        a = b;
        b = c;

        // Code prints values of a and b.
        System.out.println("a=" + a);
        System.out.println("b=" + b);
    }
}